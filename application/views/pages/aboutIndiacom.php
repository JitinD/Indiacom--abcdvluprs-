            <div class="col-md-12">
                <p class=" text-dark text-justify">
                    Since its inception in the year 2007, INDIACom has aimed at developing a strategic plan for balanced growth of  nations' economy through IT in critical areas like e-Governance, e-Commerce, Disaster Management, GIS, Nano-Technology, Intellectual Property Rights, AI and Expert Systems, Networking, Software Engineering and other emerging Technologies.
                </p>
                <p class=" text-dark text-justify">
                    The mega events, INDIACom-2007 to INDIACom-2014, received overwhelming response from participants all over the world as over 400 papers based on different aspects of computing for Nation Development were received in every edition of INDIACom. The events spanned over two days with four parallel tracks of paper presentations covering various aspects of IT and their implementation to catalyze overall growth and development process. The audience got a glimpse of limitless avenues as the participants poured in their virgin concepts of harnessing the power of computation to the tip of tooth. The hard copy of proceedings of more that 700 pages, having ISSN & ISBN serials was released during the conference.
                </p>

