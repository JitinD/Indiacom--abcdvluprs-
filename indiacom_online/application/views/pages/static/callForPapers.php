                <h1 class="text-theme">Review Process, Publication and Indexing</h1>
                <p class="generalTxt">
                    The conference aims at carrying out two rounds of review process. In the first round, the papers submitted by the authors will be assessed on the basis of their technical suitability, scope of work and plagiarism. The corresponding authors of qualifying submissions will be intimated for their papers to be double blind reviewed by three experts on the basis of originality, novelty, clarity, completeness, relevance, significance and research contribution. The shortlisted papers will be accepted for presentation and publication in the  proceedings, having
                    <b>ISSN 0973&ndash;7529</b> and <b>ISBN 978-93-80544-14-4</b> serials.  Conference proceedings will also be available in the form of CD-ROMs. INDIACom-2015 has applied for inclusion in the
                    <b>IEEE's Conference Publication Program</b>. This means that the Conference Proceedings of accepted papers (for oral presentations), which will be presented in the conference, will be submitted to
                    <font color="#FF0000"><b>IEEE Xplore</b></font>.</p>

