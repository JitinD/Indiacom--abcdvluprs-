                <h1 class="h1 text-theme">Paper Submission</h1>

                <p>
                    Authors from across different parts of the world are invited to submit their papers. Authors wishing
                    to submit their papers must refer to the website, for paper structuring and formatting guidelines in
                    detail, at <a href="technicalGuidelines.php">Technical Guidelines</a>. Authors should submit their
                    papers online at the link Submit Paper.
                    Unregistered authors should first create an account on Add Member link, to log on and submit paper.
                    Only electronic submissions will be considered. Papers submitted by e-mail will not be considered.
                </p>

                <h1 class="text-theme">Review Process, Publication and Indexing</h1>

                <p>
                    The conference aims at carrying out two rounds of review process. In the first round, the papers
                    submitted by the authors will be assessed on the basis of their technical suitability, scope of work
                    and plagiarism. The corresponding authors of qualifying submissions will be intimated for their
                    papers to be double blind reviewed by three experts on the basis of originality, novelty, clarity,
                    completeness, relevance, significance and research contribution. The shortlisted papers of all the
                    tracks (Track 1 - Track 5) will be accepted for presentation and publication in the Soft Copy of
                    Conference proceedings, bearing <strong>ISBN serial 978-93-80544-20-5</strong>, in the form of DVDs.<span
                        class="text-danger"> Accepted Papers
                    of Regular Sessions (Track 1 - Track 4) will also be published in the Hard Copy of Conference
                    Proceedings having <strong>ISSN 0973-7529</strong> and <strong>ISBN 978-93-80544-19-9</strong> serials.</span>
                    <strong>INDIACom-2016</strong> is approved by
                    <strong>IEEE's Conference Publication Program (IEEE CPP)</strong>. This means that the Conference proceedings of
                    accepted papers (for oral presentations), <strong>which will be presented in the conference</strong>, will be
                    submitted to <span class="text-danger"><strong>IEEE Xplore</strong></span>, which is indexed with world's leading Abstracting & Indexing (A&I)
                    databases, including <span class="text-danger"><strong>ISI, SCOPUS, DBLP, EI-Compendex, Google Scholar,</strong></span> etc.
                </p>
