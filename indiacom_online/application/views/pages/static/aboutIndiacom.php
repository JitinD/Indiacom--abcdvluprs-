        <p>
            The mega events, INDIACom-2007 to INDIACom-2015, received overwhelming response from participants all over
            the world as over 400 papers based on different aspects of computing for Nation Development were received in
            every edition of INDIACom. The events spanned over two days with four parallel tracks of paper presentations
            covering various aspects of IT and their implementation to catalyze overall growth and development process.
            The audience got a glimpse of limitless avenues as the participants poured in their virgin concepts of
            harnessing the power of computation to the tip of tooth. The hard copy of proceedings of more that 700
            pages, having ISSN & ISBN serials was released during the conference.
        </p>

