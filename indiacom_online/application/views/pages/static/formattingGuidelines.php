                <p>
                    Submission of a paper implies that the work described has not been published previously (except in
                    the form of an abstract or academic thesis) and is not under consideration for publication
                    elsewhere. The submission should be approved by all the authors of the paper. If a paper is finally
                    accepted, the authorities, where the work had been carried out, shall be responsible for not
                    publishing the work elsewhere in the same form. Paper, once submitted for consideration in <strong>INDIACom
                    - 2016</strong>, cannot be withdrawn unless the same is finally rejected.
                </p>

                <h3>Paper Structure and Format</h3>

                <p>While preparing and formatting papers, authors must confirm to the under-mentioned MS-Word (.doc,
                    .docx) format:-</p>
                <ul>
                    <li>The total length of the paper, including references and appendices, must
                        not exceed <strong>six (06) Letter Size pages</strong>. However, the authors can extend maximum 1 page with over length page charge,
                        if accepted. It should be typed on one-side with double column, single-line spacing, 10 font
                        size, Times New Roman, in MS Word.
                    </li>
                    <li>
                        The Top Margin should be 1", Bottom 1", Left 0.6", and Right 0.6". Page layout should be
                        portrait with 0.5 Header and Footer margins. Select the option for different Headers and Footers
                        for Odd and Even pages and different for First page in Layout (under Page Setup menu option of
                        MS Word). Authors are not supposed to write anything in the footer.
                    </li>
                    <li>
                        The title should appear in single column on the first page in 14 Font size, below which the name
                        of
                        the author(s), in bold, should be provided centrally aligned in 12 font size. The affiliations
                        of
                        all the authors and their E-mail IDs should be provided in the footer section of the first
                        column,
                        as shown in the template.
                    </li>
                    <li>
                        To avoid unnecessary errors, the authors are strongly advised to use the "spell-check" and
                        "grammar-check" functions of the word processor.
                    </li>
                    <li>
                        The complete template has been prepared and is available <a
                            href="http://bvicam.ac.in/indiacom/downloads/Template_For_Full_Paper%202015.doc">here</a>
                        for download.
                    </li>
                </ul>
                <h3>Essential Title Page Information</h3>
                <ul>
                    <li>Title: Title should be Concise and informative. Avoid abbreviations and formulae to the extent
                        possible.
                    </li>
                    <li>Authors' Names and Affiliations: Present the authors' affiliation addresses (where the actual
                        work was done) below their names. Indicate all affiliations with a lower-case superscript letter
                        immediately after the author's name and in front of the appropriate address. Provide the full
                        postal address of each affiliation, including the country name and e-mail address of each
                        author.
                    </li>
                    <li>Corresponding author: Clearly indicate who will handle correspondence at all stages of
                        refereeing and publication. Ensure that phone numbers (with country and area code) are provided
                        in addition to the e-mail address and the complete postal address.
                    </li>
                </ul>
                <h3>The structure of the paper should be based on the following details:-</h3>

                <strong>Abstract</strong>

                <p>A concise abstract not exceeding 200 words is required. The abstract should state briefly the purpose
                    of
                    the research, the principal results and major conclusions. References and non-standard or uncommon
                    abbreviations should be avoided. As a last paragraph of the abstract, 05 to 10 Index Terms, in
                    alphabetic order, under the heading Index Terms <strong>(Index Terms - …….)</strong> must be provided.
                </p>
                <strong>Nomenclature</strong>

                <p>Define all the abbreviations that are used in the paper and present a list of abbreviations with
                    their
                    definition in Nomenclature section. Ensure consistency of abbreviations throughout the article. Do
                    not
                    use any abbreviation in the paper, which has not been defined and listed in Nomenclature section.
                </p>
                <strong>Subdivision - numbered sections</strong>

                <p>Divide the paper into numbered Sections as I, II, III ... and its heading should be written in
                    CAPITAL
                    LETTERS, centrally aligned. The Subsections should be numbered A, B, C ... etc. and should be
                    written
                    in
                    the Title Case, left aligned and italicized. The Abstract, Nomenclature, Appendix, Acknowledgement
                    and
                    References will not be included in section numbering. In fact, section numbering will start from
                    Introduction and will continue till Conclusion. The Subsections should also be given a brief
                    heading,
                    written in the title case, and should appear in separate line.
                </p>
                <strong>Introduction</strong>

                <p>State the objectives of the work and provide an adequate background, with a detailed literature
                    survey
                    or a summary of the results.
                </p>
                <strong>Theory/Calculation</strong>

                <p>A Theory Section should extend, not repeat the information discussed in Introduction. In contrast, a
                    Calculation Section represents a practical development from a theoretical basis.
                </p>
                <strong>Result</strong>

                <p>Results should be clear and concise.
                </p>
                <strong>Discussion</strong>

                <p>This section should explore the importance of the results of the work, not repeat them. A combined
                    Results and Discussion section is often appropriate.
                </p>
                <strong>Conclusion and Future Scope</strong>

                <p>The main conclusions of the study may be presented in a short Conclusion Section. In this section,
                    the
                    author(s) should also briefly discuss the limitations of the research and Future Scope for
                    improvement.
                </p>
                <strong>Appendix</strong>

                <p>If there are multiple appendices, they should be identified as A, B, etc. Formulae and equations in
                    appendices should be given separate numbering: Eq. (A.1), Eq. (A.2), etc.; in a subsequent appendix,
                    Eq.
                    (B.1) and so on. Similar nomenclature should be followed for tables and figures: Table A.1; Fig.
                    A.1,
                    etc.
                </p>
                <strong>Acknowledgement</strong>

                <p>If desired, authors may provide acknowledgements at the end of the paper, before the references. The
                    organizations / individuals who provided help during the research (e.g. providing language help,
                    writing
                    assistance, proof reading the paper, sponsoring the research, etc.) may be acknowledged here.
                </p>
                <strong>References</strong>
                <ul>
                    <li>Citation in text
                        <p>Please ensure that every reference cited in the text is also present in the reference list
                            (and vice
                            versa). The references in the reference list should follow the standard IEEE reference style
                            of the
                            journal and citation of a reference.
                        </p>
                    </li>
                    <li>Web references
                        <p>As a minimum, the full URL should be given and the date when the reference was last accessed.
                            Any
                            further information, if known (DOI, author names, dates, reference to a source publication,
                            etc.),
                            should also be given. Web references can be listed separately (e.g., after the reference
                            list)
                            under a
                            different heading if desired, or can be included in the reference list, as well.</p>
                    </li>
                    <li>Reference style
                        <ul>
                            <li>Text: Indicate references by number(s) in square brackets in line with the text. The
                                actual
                                authors can
                                be referred to, but the reference number(s) must always be given. Example: '... as
                                demonstrated [3,6].
                                Barnaby and Jones [8] obtained a different result ...'
                            </li>
                            <li>
                                List: Number the references (numbers in square brackets) in the list, according to the
                                order in
                                which
                                they appear in the text.
                                Two examples, for writing reference list, are given hereunder:-<br>
                                <strong>Reference to a journal publication:</strong>
                                <br>
                                [1] I. J. Cox, J. Kilian, T. Leighton, and T. Shamoon, "Secure spread-spectrum
                                watermarking
                                for multimedia", IEEE Transactions on Image Processing, Vol. 6, No. 12, pp. 64 - 69,
                                December 1997.
                                <br>
                                <strong>Reference to a book:</strong>
                                <br>
                                [2] J. G. Proakis and D. G. Manolakis - Digital Signal Processing - Principles,
                                Algorithms and
                                Applications; Third Edition; Prentice Hall of India, 2003.
                            </li>
                        </ul>

                    </li>
                </ul>

                <strong>Mathematical Formulae</strong>

                <p>
                    Present formulae using Equation editor in the line of normal text. Number consecutively any
                    equations
                    that have to be referred in the text.
                </p>

                <strong>Captions and Numbering for Figure and Tables</strong>

                <p>Ensure that each figure / table has been numbered and captioned. Supply captions separately, not
                    attached to the figure. A caption should comprise a brief title and a description of the
                    illustration.
                    Figures and tables should be numbered separately, but consecutively in accordance with their
                    appearance
                    in the text.</p>

                <h3>Style for Illustrations</h3>

                <p>All line drawings, images, photos, figures, etc. will be published in black and white, in Hard Copy
                    of the proceedings. Authors will need to ensure that the letters, lines, etc. will remain legible,
                    even after reducing the line drawings, images, photos, figures, etc. to a two-column width, as much
                    as 4:1 from the original. However, in Soft Copy of the Proceedings, line drawings, images, photos,
                    figures, etc. may be published in colour, if requested. For this, authors will need to submit two
                    types of Camera Ready Copy (CRC), after final acceptance of their paper, one for Hard Copy
                    (compatible to black and white printing) and another for Soft Copy (compatible to colour
                    printing).</p>
