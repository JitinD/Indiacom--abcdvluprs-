                            <p class="generalTxt"><br>
                                <strong>Abstract</strong><br>
                                A concise abstract not exceeding 200 words is required. The abstract should state briefly the purpose of
                                the research, the principal results and major conclusions. References and non-standard or uncommon
                                abbreviations should be avoided. As a last paragraph of the abstract, 05 to 10 Index Terms, in
                                alphabetic order, under the heading Index Terms <strong><em>(Index Terms
                                        - &hellip;&hellip;.) </em></strong><em>must be provided.</em></p>

                            <p class="generalTxt"><strong>Nomenclature</strong> <br>
                                Define all the abbreviations that are used in the paper and present a list of abbreviations with their
                                definition in Nomenclature section. Ensure consistency of abbreviations throughout the article. Do not
                                use any abbreviation in the paper, which has not been defined and listed in Nomenclature section.</p>

                            <p class="generalTxt"><strong>Subdivision - numbered sections</strong> <br>
                                Divide the paper into numbered Sections as I, II, III, &hellip;... and its heading should be written in
                                CAPITAL LETTERS, centrally aligned. The Subsections should be numbered A, B, C, &hellip;&hellip;etc. and
                                should be written in the Title Case, left aligned and italicized. The Abstract, Nomenclature, Appendix,
                                Acknowledgement and References will not be included in section numbering. In fact, section numbering
                                will start from Introduction and will continue till Conclusion. The Subsections should also be given a
                                brief heading, written in the title case, and should appear in separate line. </p>

                            <p class="generalTxt"><strong>Introduction</strong> <br>
                                State the objectives of the work and provide an adequate background, with a detailed literature survey
                                or a summary of the results.</p>

                            <p class="generalTxt"><strong>Theory/Calculation</strong> <br>
                                A Theory Section should extend, not repeat the information discussed in Introduction. In contrast, a
                                Calculation Section represents a practical development from a theoretical basis.</p>

                            <p class="generalTxt"><strong>Result <br>
                                </strong>Results should be clear and concise.</p>

                            <p class="generalTxt"><strong>Discussion</strong> <br>
                                This section should explore the importance of the results of the work, not repeat them. A combined
                                Results and Discussion section is often appropriate. </p>

                            <p class="generalTxt"><strong>Conclusion and Future Scope</strong><br>
                                The main conclusions of the study may be presented in a short Conclusion Section. In this section, the
                                author(s) should also briefly discuss the limitations of the research and Future Scope for improvement.
                            </p>

                            <p class="generalTxt"><strong>Appendix</strong> <br>
                                If there are multiple appendices, they should be identified as A, B, etc. Formulae and equations in
                                appendices should be given separate numbering: Eq. (A.1), Eq. (A.2), etc.; in a subsequent appendix, Eq.
                                (B.1) and so on. Similar nomenclature should be followed for tables and figures: Table A.1; Fig. A.1,
                                etc.</p>

                            <p class="generalTxt"><strong>Acknowledgement </strong><br>
                                If desired, authors may provide acknowledgements at the end of the
                                paper, before the references. The organizations / individuals who provided help during the research
                                (e.g. providing language help, writing assistance, proof reading the
                                paper, sponsoring the research, etc.) may be acknowledged here.</p>

                            <p class="generalTxt"><strong>References </strong><br>
                                <strong>Citation in text</strong> <br>
                                Please ensure that every reference cited in the text is also present in the reference list (and vice
                                versa). The references in the reference list should follow the standard IEEE reference style of the
                                journal and citation of a reference.<br>
                                <strong>Web references</strong> <br>
                                As a minimum, the full URL should be given and the date when the reference was last accessed. Any
                                further information, if known (DOI, author names, dates, reference to a source publication, etc.),
                                should also be given. Web references can be listed separately (e.g., after the reference list) under a
                                different heading if desired, or can be included in the reference list, as well.<br>
                                <strong>Reference style</strong> <br>
                                Text: Indicate references by number(s) in square brackets in line with the text. The actual authors can
                                be referred to, but the reference number(s) must always be given. Example: '..... as demonstrated [3,6].
                                Barnaby and Jones [8] obtained a different result ....' <br>
                                List: Number the references (numbers in square brackets) in the list, according to the order in which
                                they appear in the text. <br>
